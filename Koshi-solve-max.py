def runge_kutta_4_system_vector(f, x0, y0_vector, x_end, h):
    xn = [x0]
    yn = [np.array(y0_vector, dtype=float)]

    x = x0
    y = np.array(y0_vector, dtype=float)

    while x < x_end:
        h_step = min(h, x_end - x)

        k1 = h_step * f(x, y)
        k2 = h_step * f(x + h_step/2, y + k1/2)
        k3 = h_step * f(x + h_step/2, y + k2/2)
        k4 = h_step * f(x + h_step, y + k3)

        y = y + (k1 + 2*k2 + 2*k3 + k4) / 6
        x += h_step

        xn.append(x)
        yn.append(y.copy())

    return np.array(xn), np.array(yn)

def solve_system_rk4(x0, y10, y20, x_end, h):
    def f1(x, y):
        y1, y2 = y
        if x <= 0:
            return 0
        return y2 * math.log(x)

    def f2(x, y):
        y1, y2 = y
        return y1 + y2**2

    def f_system(x, y_vector):
        y1, y2 = y_vector

        dy1dx = f1(x, (y1, y2))
        dy2dx = f2(x, (y1, y2))

        return np.array([dy1dx, dy2dx])

    x_vals, y_vectors = runge_kutta_4_system_vector(f_system, x0, [y10, y20], x_end, h)

    y1_vals = y_vectors[:, 0]
    y2_vals = y_vectors[:, 1]

    return x_vals, y1_vals, y2_vals

def solve_with_scipy(x0, y10, y20, x_end, h):
    def system_ode(x, y):
        y1, y2 = y
        if x <= 0:
            dy1dx = 0
        else:
            dy1dx = y2 * math.log(x)
        dy2dx = y1 + y2**2
        return [dy1dx, dy2dx]

    solution = solve_ivp(
        system_ode,
        [x0, x_end],
        [y10, y20],
        method='DOP853',
        rtol=1e-12,  # Очень высокая точность
        atol=1e-12,
        dense_output=True
    )


    x_vals = np.arange(x0, x_end + h/2, h)
    y_interp = solution.sol(x_vals)

    return x_vals, y_interp[0], y_interp[1]

def runge_error_estimate(y_h, y_h2, p=4):
    error_estimate = np.abs(y_h - y_h2) / (2**p - 1)
    return error_estimate

def get_value_at_point(x_array, y_array, point):
    """Получить значение функции в заданной точке (линейная интерполяция)"""
    idx = np.searchsorted(x_array, point) - 1
    if idx < 0:
        idx = 0
    if idx >= len(x_array) - 1:
        idx = len(x_array) - 2

    x1, x2 = x_array[idx], x_array[idx + 1]
    y1, y2 = y_array[idx], y_array[idx + 1]

    if x2 == x1:
        return float(y1)

    t = (point - x1) / (x2 - x1)
    return float(y1 + t * (y2 - y1))

