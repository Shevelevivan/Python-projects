def lagrange_interpolation(x_points, y_points, x_values):

    # Преобразуем входные данные в массивы numpy
    x_points = np.asarray(x_points, dtype=float)
    y_points = np.asarray(y_points, dtype=float)
    x_values = np.asarray(x_values, dtype=float)

    # Проверка входных данных
    if len(x_points) != len(y_points):
        raise ValueError(f"Количество x-точек ({len(x_points)}) должно совпадать с количеством y-точек ({len(y_points)})")

    if len(x_points) == 0:
        raise ValueError("Массивы точек не могут быть пустыми")

    if len(x_points) == 1:
        # Если только одна точка, полином - константа
        return np.full_like(x_values, y_points[0])

    # Проверка на уникальность узлов интерполяции
    if len(np.unique(x_points)) != len(x_points):
        raise ValueError("Узлы интерполяции должны быть уникальными")

    # Если x_values - скаляр, преобразуем в массив
    is_scalar = x_values.ndim == 0
    x_values = np.atleast_1d(x_values)

    n = len(x_points)
    result = np.zeros_like(x_values, dtype=float)

    # Основной цикл интерполяции
    for i in range(n):
        # Вычисляем базисный полином Лагранжа l_i(x)
        term = np.ones_like(x_values, dtype=float) * y_points[i]

        for j in range(n):
            if i != j:
                term *= (x_values - x_points[j]) / (x_points[i] - x_points[j])

        result += term

    # Возвращаем результат в том же формате, что и вход
    return result[0] if is_scalar else result

def lagrange_basis_polynomials(x_points, x_values):

    x_points = np.asarray(x_points, dtype=float)
    x_values = np.asarray(x_values, dtype=float)

    n = len(x_points)
    m = len(x_values)
    basis = np.zeros((n, m), dtype=float)

    for i in range(n):
        basis[i] = np.ones(m, dtype=float)
        for j in range(n):
            if i != j:
                basis[i] *= (x_values - x_points[j]) / (x_points[i] - x_points[j])

    return basis

def lagrange_coefficients(x_points, y_points):

    x_points = np.asarray(x_points, dtype=float)
    y_points = np.asarray(y_points, dtype=float)
    n = len(x_points)

    # Инициализируем массив коэффициентов
    coefficients = np.zeros(n, dtype=float)

    for i in range(n):
        # Получаем базисный полином l_i(x)
        poly = np.array([1.0], dtype=float)  # Начинаем с константы 1

        for j in range(n):
            if i != j:
                # Умножаем на (x - x_j)/(x_i - x_j)
                denominator = 1.0 / (x_points[i] - x_points[j])

                # Создаем полином (x - x_j) * denominator
                term = np.array([-x_points[j] * denominator, denominator], dtype=float)

                # Умножаем текущий полином на term
                new_poly = np.zeros(len(poly) + 1, dtype=float)
                for k in range(len(poly)):
                    for l in range(2):
                        new_poly[k + l] += poly[k] * term[l]
                poly = new_poly

        # Добавляем вклад от y_i * l_i(x)
        for k in range(len(poly)):
            coefficients[n - 1 - k] += y_points[i] * poly[len(poly) - 1 - k]

    return coefficients

class LagrangeInterpolator:

    def __init__(self, x_points, y_points):

        self.x_points = np.asarray(x_points, dtype=float)
        self.y_points = np.asarray(y_points, dtype=float)
        self.n = len(x_points)

        # Проверка данных
        if len(self.x_points) != len(self.y_points):
            raise ValueError("Количество x и y точек должно совпадать")

        if len(np.unique(self.x_points)) != self.n:
            raise ValueError("Узлы интерполяции должны быть уникальными")

        # Предвычисляем знаменатели для оптимизации
        self.denominators = np.ones(self.n, dtype=float)
        for i in range(self.n):
            denominator = 1.0
            for j in range(self.n):
                if i != j:
                    denominator *= (self.x_points[i] - self.x_points[j])
            self.denominators[i] = denominator

    def __call__(self, x_values):

        x_values = np.asarray(x_values, dtype=float)
        is_scalar = x_values.ndim == 0
        x_values = np.atleast_1d(x_values)

        result = np.zeros_like(x_values, dtype=float)

        for i in range(self.n):
            # Вычисляем числитель для текущего базисного полинома
            numerator = np.ones_like(x_values, dtype=float)
            for j in range(self.n):
                if i != j:
                    numerator *= (x_values - self.x_points[j])

            # Вычисляем базисный полином и добавляем в результат
            result += self.y_points[i] * numerator / self.denominators[i]

        return result[0] if is_scalar else result

    def derivative(self, x_values, order=1):

        # Для производных используем численное дифференцирование
        # Можно было бы аналитически вычислить производную полинома
        h = 1e-6
        x_values = np.asarray(x_values, dtype=float)

        if order == 1:
            return (self(x_values + h) - self(x_values - h)) / (2 * h)
        elif order == 2:
            return (self(x_values + h) - 2 * self(x_values) + self(x_values - h)) / (h ** 2)
        else:
            raise ValueError("Пока поддерживаются только производные 1-го и 2-го порядка")

    def get_coefficients(self):
        return lagrange_coefficients(self.x_points, self.y_points)

    def plot(self, x_min=None, x_max=None, num_points=1000, show_nodes=True):

        if x_min is None:
            x_min = self.x_points.min() - 0.5
        if x_max is None:
            x_max = self.x_points.max() + 0.5

        x_plot = np.linspace(x_min, x_max, num_points)
        y_plot = self(x_plot)

        plt.figure(figsize=(10, 6))
        plt.plot(x_plot, y_plot, 'b-', linewidth=2, label='Интерполяционный полином')

        if show_nodes:
            plt.scatter(self.x_points, self.y_points, color='red', s=100,
                       zorder=5, label='Узлы интерполяции', edgecolors='black')

        plt.xlabel('x', fontsize=12)
        plt.ylabel('L(x)', fontsize=12)
        plt.title(f'Интерполяционный полином Лагранжа степени {self.n-1}', fontsize=14)
        plt.grid(True, alpha=0.3)
        plt.legend()
        plt.tight_layout()
        plt.show()

